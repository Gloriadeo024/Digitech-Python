# Add the number 10 to the end of the list.
numbers = [4, 7, 2, 9, 1]
numbers.append(10)
print(numbers)

# Remove the number 2 from the list.
numbers.remove(2)
print(numbers)

# Sort the list in ascending order.
numbers.sort()
print(numbers)

# Print the final list after all changes.
print("Final list:", numbers)
